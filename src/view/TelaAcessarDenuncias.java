    private void preencherTabela(){
    DenunciaDAO denunciaDAO = new DenunciaDAO();
    List<Denuncia> listaDenuncia = denunciaDAO.getDenuncia();
    DefaultTableModel tbProduto = (DefaultTableModel) jtDenuncia.getModel(); 
    
    for(Denuncia p : listaDenuncia){
        Object[] obj = new Object[]{
        p.getUsername(),
        p.getDescricao(),
        p.getLocal(),
        p.getCep()
        };
        tbProduto.addRow(obj);
    }
}

